package it.unibo.artificial_vision_tracking.robot;

/**
 * Enum to define the direction of the robot.
 */
public enum DirectionEnum {
    /**
     * Move forward.
     */
    MOVE_FORWARD,
    /**
     * Move backward.
     */
    MOVE_BACKWARD,
    /**
     * Turn left.
     */
    TURN_LEFT,
    /**
     * Turn right.
     */
    TURN_RIGHT,
    /**
     * Stop the robot.
     */
    STOP
}
